    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String cd_name = jTextField1.getText().toString();

        if (jRadioButton1.isSelected()) {

            if (jTextField1.getText().trim().isEmpty() || jTextField2.getText().trim().isEmpty() || jTextField3.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please, Enter all the info", "Input Error", JOptionPane.ERROR_MESSAGE);
            } else {

                try {
                    int cd_quantity = Integer.parseInt(jTextField2.getText());
                    float movie_duration = Float.parseFloat(jTextField3.getText());

                    CD obj = new Movie(cd_name, cd_quantity, movie_duration);
                    Assignment.CDs.add(obj);
                    JOptionPane.showMessageDialog(Assignment.frame, "The Movie is added successfully");
                    jTextField1.setText(null);
                    jTextField2.setText(null);
                    jTextField3.setText(null);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "You must enter quantity and movie duration as numbers", "Input Error", JOptionPane.ERROR_MESSAGE);
                }

            }

        } else {

            if (jTextField1.getText().trim().isEmpty() || jTextField2.getText().trim().isEmpty() || jTextField4.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please, Enter all the info", "Input Error", JOptionPane.ERROR_MESSAGE);
            } else {

                try {
                    int cd_quantity = Integer.parseInt(jTextField2.getText());
                    int series_numOfEpisodes = Integer.parseInt(jTextField4.getText());

                    CD obj = new Series(cd_name, cd_quantity, series_numOfEpisodes);
                    Assignment.CDs.add(obj);
                    JOptionPane.showMessageDialog(Assignment.frame, "The Series is added successfully");
                    jTextField1.setText(null);
                    jTextField2.setText(null);
                    jTextField4.setText(null);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "You must enter quantity  as numbers", "Input Error", JOptionPane.ERROR_MESSAGE);
                }

            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

