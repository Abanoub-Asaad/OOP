    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String cd_name = jTextField1.getText().toString();
        boolean flag = false;

        for (CD cd_obj : Assignment.CDs) {
            if (cd_name.equals(cd_obj.getName())) {
                flag = true;
                if (cd_obj.getQuanitity() > 0) {
                    cd_obj.setQuanitity(cd_obj.getQuanitity() - 1);
                    JOptionPane.showMessageDialog(Assignment.frame, "Rented");
                } else {
                    JOptionPane.showMessageDialog(Assignment.frame, "This CD isn't exist in the stock");
                }
                break;
            }

        }

        if (!flag) {
            if (jTextField1.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please, Enter all the info", "Input Error", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "The CD isn't exist", "Input Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

